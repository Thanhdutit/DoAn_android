package com.midterm.appnauan1;

public class Food {
    String avatar;
    String name;
    int id;
    public Food(String avatar, String name,int id) {
        this.avatar = avatar;
        this.name = name;
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
